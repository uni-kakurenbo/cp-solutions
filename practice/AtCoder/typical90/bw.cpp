#include "template/standard.hpp"

void solve();

signed main() {
    debug(__cplusplus);
    int $ = 1;
    // std::cin >> $;
    for(int _ = 0; _ < $; ++_) {
        debug_("Case: #" + std::to_string(_));
        solve();
    }
    return 0;
}

void solve() {
    i64 n; input >> n;
    auto facts = lib::count_factors(n);
    debug(facts);

    u64 cnt = 0;

    ITR(v, c, facts) {
        cnt += c;
    }

    print(std::bit_width(cnt - 1));
}
