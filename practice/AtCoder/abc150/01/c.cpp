#include "template/standard.hpp"

void solve();

signed main() {
    debug(__cplusplus);
    int $ = 1;
    // std::cin >> $;
    for(int _ = 0; _ < $; ++_) {
        debug_("Case: #" + std::to_string(_));
        solve();
    }
    return 0;
}

void solve() {
    int n; input >> n;
    vector<i64> p(n), q(n); input >> p >> q;
    if(q < p) std::swap(p, q);

    int ans = 0;
    while(p != q) std::next_permutation(ALL(p)), ++ans;

    print(ans);
}
